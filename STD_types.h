#ifndef _STD_TYPES_H_
#define _STD_TYPES_H_

typedef unsigned char 			u8;
typedef unsigned short int  	u16;
typedef unsigned int  			u32;
typedef unsigned long int  		u64;

typedef char 					s8;
typedef short int  				s16;
typedef int  					s32;
typedef long int  				s64;

typedef float  					f32;
typedef double  				f64;

#endif